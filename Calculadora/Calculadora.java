package Calculadora;

import java.lang.Math;

public class Calculadora {
	
	
	public int suma(int a,int b)
	{
		return a+b;
	}

	
	public int cuadrado(int a)
	{
		return (int)Math.pow(a, 2);
	}
	
	
	public int resta(int a, int b)
	{
		return a-b;
		
	}
	
	
	public int negativo(int a)
	{
		return -1*a;
	
	}
	
	
}

